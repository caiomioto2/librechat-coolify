# Handle script arguments
case "${1:-}" in
    "clean")
        log "Cleaning git repository (removes all git history)..."
        rm -rf .git
        success "Git repository cleaned"
        ;;
    "status")
        log "Git repository status:"
        git status
        ;;
    *)
        main
        ;;
esac